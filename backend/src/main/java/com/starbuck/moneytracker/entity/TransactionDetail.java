package com.starbuck.moneytracker.entity;

import java.math.BigDecimal;
import java.time.LocalDateTime;

import org.hibernate.annotations.CreationTimestamp;
import org.hibernate.annotations.UpdateTimestamp;
import org.jspecify.annotations.NonNull;

import jakarta.persistence.Id;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;

@Entity(name = "transaction_details")
public class TransactionDetail {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false)
    private String name;

    @Column(nullable = true, precision = 10, scale = 2)
    private BigDecimal price;

    @Column(nullable = true, precision = 10, scale = 2)
    private BigDecimal weight;

    @Column(nullable = true, precision = 10, scale = 2)
    private BigDecimal unitPrice;

    // TODO category field

    @CreationTimestamp
    private LocalDateTime createdAt;

    @UpdateTimestamp
    private LocalDateTime updatedAt;

    @ManyToOne
    @JoinColumn(name = "transaction_id", nullable = false)
    private Transaction transaction;

    /**
     * Visszatér a tranzakció összegével
     * 
     * @return
     */
    public @NonNull BigDecimal getCost() throws IllegalStateException {
        if (this.price != null) {
            return this.price;
        } else if (this.weight != null && this.unitPrice != null) {
            return this.calculateCostFromUnitPriceAndWeight();
        } else {
            throw new IllegalStateException(
                    "Price is null and weight or unitPrice is not defined for TransactionDetail with ID: " + this.id);
        }
    }

    /**
     * Kiszámolja a termék összegét, a súly és egységár használatával
     * 
     * @return
     */
    public @NonNull BigDecimal calculateCostFromUnitPriceAndWeight() {
        if (this.weight == null || this.unitPrice == null) {
            throw new IllegalStateException(
                    "TransactionDetail must have both weight and unitPrice defined to calculate cost.");
        }

        return this.weight.multiply(this.unitPrice);
    }

    // AUTOGENERATED

    public TransactionDetail() {
    }

    public TransactionDetail(Long id, String name, BigDecimal price, BigDecimal weight, BigDecimal unitPrice, Transaction transaction) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.weight = weight;
        this.unitPrice = unitPrice;
        this.transaction = transaction;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public BigDecimal getWeight() {
        return weight;
    }

    public void setWeight(BigDecimal weight) {
        this.weight = weight;
    }

    public BigDecimal getUnitPrice() {
        return unitPrice;
    }

    public void setUnitPrice(BigDecimal unitPrice) {
        this.unitPrice = unitPrice;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public LocalDateTime getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(LocalDateTime updatedAt) {
        this.updatedAt = updatedAt;
    }

    public Transaction getTransaction() {
        return transaction;
    }

    public void setTransaction(Transaction transaction) {
        this.transaction = transaction;
    }
}
